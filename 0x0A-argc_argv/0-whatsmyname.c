#include <stdio.h>

/**
 * main - Entry point. Prints the name of the program.
 * @argc: Argument count (unused)
 * @argv: Array of arguments (argv[0] holds the program name)
 *
 * Return: Always 0
 */
int main(int argc __attribute__((unused)), char *argv[])
{
printf("%s\n", argv[0]);
return (0);
}
